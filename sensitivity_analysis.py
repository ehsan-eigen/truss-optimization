class StructEnvironment():
    def __init__(self, dataset=DEFAULT_DATASET):
        # `dataset` is a directory containing nodes.csv, members.csv,
        # support.csv (restrained DOFs, 0-indexed) and loads.csv (sparse
        # `dof,value` rows, 0-indexed).

        # Constants
        self.E = 200 * 10**6  # (N/m^2) Young's modulus
        self.step_size = 0.01
        self.A = 0.01  # default size of element

        self.nodes = np.genfromtxt(f'{dataset}/nodes.csv', delimiter=',', dtype=np.float64)
        self.members = np.genfromtxt(f'{dataset}/members.csv', delimiter=',', dtype=np.int32)
        self.restrained_DOF = np.atleast_1d(
            np.genfromtxt(f'{dataset}/support.csv', delimiter=',', dtype=np.int32)
        )

        self.DOF = calc_DOF(self.members)
        self.element_count = len(self.members)

        # Loading: sparse `dof,value` rows
        self.point_loads = np.zeros(self.DOF)
        loads = np.atleast_2d(
            np.genfromtxt(f'{dataset}/loads.csv', delimiter=',', dtype=np.float64)
        )
        for dof, value in loads:
            self.point_loads[int(dof)] = value

        # Define any additional environment-specific variables here
        self.current_step = 0
        self.rotations, self.lengths = calc_rotation_length(self.members, self.nodes)

        # Constraint
        self.total_area = sum(self.A*self.lengths)

    def analyse(self,members_area):
        K = build_K(self.members, members_area, self.rotations, self.lengths, self.E)
        U_free = calc_displacement(K, self.point_loads, self.restrained_DOF)
        UG = assemble_UG(U_free, self.DOF, self.restrained_DOF)
        U_2d = UG.reshape((-1,2))
        self.deflections = []
        def_members = np.zeros((self.element_count))
        F_members = np.zeros((self.element_count))
        for i in range(self.element_count):
            L = self.lengths[i]
            F_member,def_member =  calc_member_forces_def(self.members[i],members_area[i],self.rotations[i],L,U_2d,self.E)
            def_members[i] = def_member/L
            F_members[i] = F_member
        return F_members,def_members,UG,K

    def calculate_cost(self,UG): # global compliance
        return UG@self.point_loads

    def reset(self):
        self.current_step +=1
        members_area = np.random.normal(loc=self.A,scale=self.A/3,size=len(self.members))
        members_area[members_area<1e-4] = 1e-4
        members_area = self.redistribute_material(members_area)
        return members_area

    def redistribute_material(self,members_area):
        members_area = members_area * (self.total_area/sum(members_area*self.lengths))
        return members_area

    def update_area(self,members_area):
        random_element = np.random.randint(self.element_count)
        members_area[random_element] = members_area[random_element] + np.random.normal(loc=0,scale=self.A/5)
        members_area[members_area<1e-4] = 1e-4
        members_area = self.redistribute_material(members_area)
        return members_area

    def step(self, members_area):
        F_members,def_members,UG,K = self.analyse(members_area)
        cost = self.calculate_cost(def_members)
        return cost,F_members,UG

    def render(self, members_area, F_members, UG, output_dir=OUTPUT_DIR,
               compliance=None, volume=None, algo=None):
        plot_deflection(self.members, self.nodes, F_members, members_area, UG,
                        xFac, self.current_step, output_dir=output_dir,
                        compliance=compliance, volume=volume, algo=algo)

"""# Adjoint Method ( Sensivity Analysis )

Compliance J = F^T U is a SELF-ADJOINT problem: K is symmetric, dJ/dU = F,
so the adjoint equation K^T λ = F yields λ = U. The full adjoint sensitivity
formula −λ^T (dK/dA_e) U then collapses to a per-element closed form
involving only the local axial strain — no global dK/dA tensor and no second
linear solve are needed.

For ANY OTHER objective (peak stress, displacement at a node, eigenvalue,
buckling load, etc.) this shortcut does NOT apply: you must solve the
adjoint system K^T λ = ∂J/∂U and form −λ^T (dK/dA_e) U explicitly.
"""

def gradient(def_members, lengths, E):
    # Self-adjoint compliance shortcut.
    # dK_e/dA_e = (E/L_e) b_e b_e^T with b_e^T u_e = axial elongation = strain·L_e,
    # so dJ/dA_e = −(E/L_e)(b_e^T u_e)^2 = −E · L_e · strain_e^2.
    return -E * lengths * def_members**2


# ---------------------------------------------------------------------------
# General adjoint machinery — use these for non-self-adjoint objectives
# (peak stress, nodal displacement, eigenvalues, buckling, etc.).
# For compliance with fixed loads, prefer the `gradient()` shortcut above.
# ---------------------------------------------------------------------------

def calculate_del_Kg__del_area(theta, mag, E, A):
    c = math.cos(theta)
    s = math.sin(theta)

    K11 = (E/mag)*np.array([[c**2,c*s],[c*s,s**2]])
    K12 = (E/mag)*np.array([[-c**2,-c*s],[-c*s,-s**2]])
    K21 = (E/mag)*np.array([[-c**2,-c*s],[-c*s,-s**2]])
    K22 = (E/mag)*np.array([[c**2,c*s],[c*s,s**2]])

    return [K11, K12, K21, K22]


def build_del_K__del_area(members, members_area, orientations, lengths, E):
    n_DOF = calc_DOF(members)
    Kp = np.zeros([n_DOF, n_DOF])
    d_Kp__d_area = np.zeros((n_DOF, n_DOF, len(members)))
    for dimension in range(len(members)):
        for n, mbr in enumerate(members):
            theta = orientations[n]
            L = lengths[n]
            A = members_area[n]
            if n == dimension:
                [K11, K12, K21, K22] = calculate_del_Kg__del_area(theta, L, E, A)
            else:
                continue

            node_i = mbr[0]
            node_j = mbr[1]
            i = 2 * node_i
            j = 2 * node_j

            d_Kp__d_area[i:i+2, i:i+2, dimension] = Kp[i:i+2, i:i+2] + K11
            d_Kp__d_area[j:j+2, j:j+2, dimension] = Kp[j:j+2, j:j+2] + K22
            d_Kp__d_area[i:i+2, j:j+2, dimension] = Kp[i:i+2, j:j+2] + K12
            d_Kp__d_area[j:j+2, i:i+2, dimension] = Kp[j:j+2, i:i+2] + K21

    return d_Kp__d_area


def gradient_general(K, dJ_dU, U, del_K__del_area, lengths, restrained_DOF):
    # Full adjoint sensitivity for an arbitrary objective J(U, A).
    # Caller supplies ∂J/∂U (vector over all DOF). Solves K^T λ = ∂J/∂U,
    # then dJ/dA_e = ∂J/∂A_e − λ^T (∂K/∂A_e) U  (the explicit ∂J/∂A_e term
    # must be added by the caller if it is nonzero). in other words, 
    # it returns only the −λ^T (∂K/∂A_e) U term. 
    # If your future J has explicit dependence on A (e.g., a volume penalty, or a stress
    # formula that divides by A), you'll need to add ∂J/∂A_e at the call site.
    K_reduced = build_matrix_reduced(K, restrained_DOF)
    dKdA_reduced = build_matrix_reduced(del_K__del_area, restrained_DOF)
    dJdU_reduced = build_matrix_reduced(dJ_dU, restrained_DOF)
    U_reduced = build_matrix_reduced(U, restrained_DOF)

    lambda_ = np.linalg.solve(K_reduced.T, dJdU_reduced)

    temp = np.zeros((len(U_reduced), len(lengths)))
    for i in range(len(lengths)):
        temp[:, i] = dKdA_reduced[:, :, i] @ U_reduced

    return -lambda_.T @ temp


class MMAWeightMin:
    # Simplified Method of Moving Asymptotes (Svanberg 1987) for
    #   minimize    Σ A_e L_e                (structural weight ∝ volume)
    #   subject to  compliance(A) ≤ C_max
    #               A_min ≤ A_e ≤ A_max
    #
    # With a single global inequality constraint, the MMA subproblem reduces
    # to a 1-D dual (one Lagrange multiplier λ ≥ 0) and is solved by bisection
    # in each outer iteration — no QP solver needed. Asymptotes L_e, U_e are
    # adapted based on the sign of the recent move history.
    #
    # Use this solver for weight reduction. For fixed-volume compliance
    # minimization, use `oc_update()` instead.

    def __init__(self, A0, A_min=1e-4, A_max=None, move=0.2,
                 asy_init=0.5, asy_decr=0.7, asy_incr=1.2,
                 asy_floor_frac=0.1, asy_ceil_frac=10.0):
        # `asy_floor_frac` / `asy_ceil_frac` keep asymptotes within an order
        # of magnitude of A (essential when design variables are positive
        # quantities spanning several orders of magnitude — e.g. truss areas).
        self.A_min = A_min
        self.A_max = A_max if A_max is not None else 100.0 * A0.max()
        self.move = move
        self.asy_init = asy_init
        self.asy_decr = asy_decr
        self.asy_incr = asy_incr
        self.asy_floor_frac = asy_floor_frac
        self.asy_ceil_frac = asy_ceil_frac
        self.A_prev = A0.copy()
        self.A_prev2 = A0.copy()
        self.L = None
        self.U = None
        self.k = 0

    def _update_asymptotes(self, A):
        rng = self.A_max - self.A_min
        if self.k < 2:
            self.L = A - self.asy_init * rng
            self.U = A + self.asy_init * rng
        else:
            sgn = (A - self.A_prev) * (self.A_prev - self.A_prev2)
            gamma = np.where(sgn < 0, self.asy_decr,
                     np.where(sgn > 0, self.asy_incr, 1.0))
            self.L = A - gamma * (self.A_prev - self.L)
            self.U = A + gamma * (self.U - self.A_prev)
        # Cap asymptote distance relative to A (positivity / scale safety) and
        # also relative to the total range (Svanberg's standard caps).
        self.L = np.maximum(self.L, self.asy_floor_frac * A)
        self.L = np.minimum(self.L, A - 0.01 * rng)
        self.L = np.maximum(self.L, A - 10.0 * rng)
        self.U = np.minimum(self.U, self.asy_ceil_frac * A)
        self.U = np.maximum(self.U, A + 0.01 * rng)
        self.U = np.minimum(self.U, A + 10.0 * rng)

    def step(self, A, lengths, compliance, dC_dA, C_max, _update_asy=True):
        # Returns A_new minimizing the MMA approximation of the volume
        # objective subject to the MMA approximation of compliance ≤ C_max.
        # Pass _update_asy=False to reuse the current L/U (used by GCMMA
        # backtracking, which contracts L/U externally between retries).
        if _update_asy:
            self._update_asymptotes(A)
        L, U = self.L, self.U
        rng = self.A_max - self.A_min

        # Move-limited box for the subproblem (inside the asymptote interval).
        alpha = np.maximum.reduce([np.full_like(A, self.A_min),
                                   L + 0.1 * (A - L),
                                   A - self.move * rng])
        beta  = np.minimum.reduce([np.full_like(A, self.A_max),
                                   U - 0.1 * (U - A),
                                   A + self.move * rng])

        # MMA coefficients. Objective grad = lengths (>0) → only p0 is nonzero.
        # Constraint grad dC/dA ≤ 0 (more material → less compliance) → only q1 is nonzero.
        g0 = lengths
        g1 = dC_dA
        p0 = (U - A)**2 * np.maximum(g0, 0.0)
        q0 = (A - L)**2 * np.maximum(-g0, 0.0)
        p1 = (U - A)**2 * np.maximum(g1, 0.0)
        q1 = (A - L)**2 * np.maximum(-g1, 0.0)

        # Constraint approx:  C(A) ≈ C_k + Σ[p1/(U-A) + q1/(A-L)] − Σ[p1/(U-A_k) + q1/(A_k-L_k)]
        # Require ≤ C_max ⇒ Σ[p1/(U-A) + q1/(A-L)] ≤ rhs
        rhs = C_max - compliance + np.sum(p1 / (U - A) + q1 / (A - L))

        def primal(lam):
            # Stationarity: p0/(U-A)^2 + lam·p1/(U-A)^2 = q0/(A-L)^2 + lam·q1/(A-L)^2
            num_top = p0 + lam * p1
            num_bot = q0 + lam * q1
            r = np.sqrt(np.maximum(num_bot, 1e-30) / np.maximum(num_top, 1e-30))
            # (A-L)/(U-A) = r ⇒ A = (L + r·U) / (1 + r)
            A_star = (L + r * U) / (1.0 + r)
            return np.clip(A_star, alpha, beta)

        def constraint_residual(lam):
            A_l = primal(lam)
            return np.sum(p1 / (U - A_l) + q1 / (A_l - L)) - rhs

        # Bisect λ ≥ 0. As λ↑, A↑, LHS↓ (monotone), so this is well-behaved.
        if constraint_residual(0.0) <= 0:
            A_new = primal(0.0)                      # constraint inactive
        else:
            lo, hi = 0.0, 1.0
            while constraint_residual(hi) > 0 and hi < 1e20:
                hi *= 10.0
            for _ in range(60):
                mid = 0.5 * (lo + hi)
                if constraint_residual(mid) > 0:
                    lo = mid
                else:
                    hi = mid
            A_new = primal(0.5 * (lo + hi))

        if _update_asy:
            self.A_prev2 = self.A_prev
            self.A_prev = A.copy()
            self.k += 1
        return A_new


def oc_update(A, dJ_dA, lengths, V_target,
              A_min=1e-4, A_max=None, move=0.2, eta=0.5,
              mu_lo=1e-12, mu_hi=1e12, tol=1e-6):
    # Optimality-Criteria update for compliance minimization under a fixed
    # volume constraint Σ A_e L_e = V_target.
    #
    # Update rule:  A_new = clip( A · (−dJ/dA / (μ L))^η,
    #                             max(A_min, A(1−move)),
    #                             min(A_max, A(1+move)) )
    # μ is found by bisection so the volume constraint is met. For compliance
    # dJ/dA ≤ 0 so −dJ/dA ≥ 0 and the root inside the parentheses is real.
    if A_max is None:
        A_max = 100.0 * A.max()
    neg_dJ = np.maximum(-dJ_dA, 0.0)
    lo, hi = mu_lo, mu_hi
    while (hi - lo) / (hi + lo) > tol:
        mu = 0.5 * (lo + hi)
        B_eta = (neg_dJ / (mu * lengths + 1e-30)) ** eta
        A_trial = A * B_eta
        A_new = np.minimum(A_trial, np.minimum(A_max, A * (1.0 + move)))
        A_new = np.maximum(A_new,   np.maximum(A_min, A * (1.0 - move)))
        if np.sum(A_new * lengths) - V_target > 0:
            lo = mu  # too much volume → tighten penalty
        else:
            hi = mu
    return A_new


def run_sqp(env, members_area, C_max, max_iter=200, A_min=1e-4, A_max=None,
            ftol=1e-9, output_dir=OUTPUT_DIR):
    # Minimize weight Σ A_e L_e subject to:
    #   compliance(A) ≤ C_max  (nonlinear inequality)
    #   A_min ≤ A_e ≤ A_max   (bounds)
    # Uses scipy's SLSQP with the analytical gradient already in this file.
    #
    # Scaling: work in x = A / A_ref (dimensionless), objective scaled by V0,
    # constraint scaled by C_max. This brings all quantities to O(1), which is
    # critical for SLSQP's QP subproblem — compliance (~10^6) vs area (~10^-2)
    # causes very poor steps without scaling.
    #
    # A cache keyed on x.tobytes() avoids re-running FEM when scipy evaluates
    # obj and jac at the same point (common during line search).
    from scipy.optimize import minimize, Bounds

    if A_max is None:
        A_max = 100.0 * members_area.max()

    A_ref = members_area.copy()
    V0 = float(np.sum(A_ref * env.lengths))         # initial weight (= V_target for MMA start)

    compliance_hist, volume_hist = [], []
    _cache = {}

    def _analyse(x):
        A = x * A_ref
        key = x.tobytes()
        if key not in _cache:
            _cache.clear()
            F_members, def_members, UG, K = env.analyse(A)
            compliance = env.calculate_cost(UG)
            dC_dA = gradient(def_members, env.lengths, env.E)
            _cache[key] = (compliance, dC_dA, F_members, UG, A)
        return _cache[key]

    # Objective: scaled weight  →  grad w.r.t. x_e = A_ref_e * L_e / V0
    def obj(x):
        A = x * A_ref
        return float(np.sum(A * env.lengths)) / V0

    def obj_jac(x):
        return A_ref * env.lengths / V0

    # Constraint: C_max - compliance(x) ≥ 0  (scipy 'ineq' convention)
    # grad w.r.t. x_e = -dC/dA_e * A_ref_e / C_max
    def con_fun(x):
        compliance, *_ = _analyse(x)
        return (C_max - compliance) / C_max

    def con_jac(x):
        _, dC_dA, *_ = _analyse(x)
        return -dC_dA * A_ref / C_max

    def callback(x):
        compliance, _, F_members, UG, A = _analyse(x)
        volume = float(np.sum(A * env.lengths))
        feas = "feas" if compliance <= C_max else "INFEAS"
        compliance_hist.append(compliance)
        volume_hist.append(volume)
        env.render(A, F_members, UG, output_dir=output_dir,
                   compliance=compliance, volume=volume, algo='SQP')
        print(f"[SQP] iter {len(compliance_hist):3d}  vol {volume:.4f}  "
              f"compliance {compliance:.4f} (≤ {C_max:.4f}, {feas})")
        env.current_step += 1

    x0 = np.ones(len(members_area))
    result = minimize(
        obj, x0, method='SLSQP', jac=obj_jac,
        bounds=Bounds(lb=A_min / A_ref, ub=A_max / A_ref),
        constraints={'type': 'ineq', 'fun': con_fun, 'jac': con_jac},
        callback=callback,
        options={'maxiter': max_iter, 'ftol': ftol},
    )
    print(f"[SQP] {'converged' if result.success else 'stopped'}: {result.message}")
    return result.x * A_ref, np.array(compliance_hist), np.array(volume_hist)


def run_oc(env, members_area, max_iter=150, change_tol_factor=1e-3,
           output_dir=OUTPUT_DIR):
    V_target = env.total_area
    change_tol = change_tol_factor * env.A
    compliance_hist, volume_hist = [], []
    for i in range(max_iter):
        F_members, def_members, UG, K = env.analyse(members_area)
        compliance_hist.append(env.calculate_cost(UG))
        volume_hist.append(float(np.sum(members_area * env.lengths)))
        env.render(members_area, F_members, UG, output_dir=output_dir,
                   compliance=compliance_hist[-1], volume=volume_hist[-1], algo='OC')

        dJ_dA = gradient(def_members, env.lengths, env.E)
        A_new = oc_update(members_area, dJ_dA, env.lengths, V_target)

        change = np.abs(A_new - members_area).max()
        print(f"[OC]  iter {i:3d}  compliance {compliance_hist[-1]:.4f}  "
              f"vol {volume_hist[-1]:.4f}  ΔA {change:.3e}")
        members_area = A_new
        env.current_step += 1
        if change < change_tol:
            print(f"[OC]  converged at iteration {i}")
            break
    return members_area, np.array(compliance_hist), np.array(volume_hist)


def run_mma(env, members_area, C_max, max_iter=200, change_tol_factor=1e-3,
            A_min=1e-4, A_max=None, overshoot_factor=1.01, max_inner=15,
            output_dir=OUTPUT_DIR):
    # Outer loop = MMA iteration; inner loop = GCMMA-style backtracking. If
    # the trial step makes the true compliance exceed C_max * overshoot_factor,
    # we reject it, contract the asymptotes toward A_k, and re-solve. This
    # safeguards against MMA's linear approximation underestimating compliance
    # growth (compliance ~ 1/A is highly nonlinear).
    solver = MMAWeightMin(members_area, A_min=A_min, A_max=A_max)
    change_tol = change_tol_factor * env.A
    compliance_hist, volume_hist = [], []
    for i in range(max_iter):
        F_members, def_members, UG, K = env.analyse(members_area)
        compliance = env.calculate_cost(UG)
        compliance_hist.append(compliance)
        volume_hist.append(float(np.sum(members_area * env.lengths)))
        env.render(members_area, F_members, UG, output_dir=output_dir,
                   compliance=compliance, volume=volume_hist[-1], algo='MMA')

        dC_dA = gradient(def_members, env.lengths, env.E)

        # GCMMA-style inner loop: try a step, if it overshoots C_max badly,
        # contract asymptotes and try again.
        A_new = solver.step(members_area, env.lengths, compliance, dC_dA, C_max)
        for inner in range(max_inner):
            F_t, d_t, UG_t, _ = env.analyse(A_new)
            C_trial = env.calculate_cost(UG_t)
            if C_trial <= C_max * overshoot_factor:
                break
            # Pull asymptotes inward toward A_k (Svanberg's GCMMA "rho" idea
            # simplified): shrink the (U-L) interval by half each retry.
            solver.L = members_area - 0.5 * (members_area - solver.L)
            solver.U = members_area + 0.5 * (solver.U - members_area)
            A_new = solver.step(members_area, env.lengths, compliance, dC_dA,
                                C_max, _update_asy=False)

        change = np.abs(A_new - members_area).max()
        feas = "feas" if compliance <= C_max else "INFEAS"
        print(f"[MMA] iter {i:3d}  vol {volume_hist[-1]:.4f}  "
              f"compliance {compliance:.4f} (≤ {C_max:.4f}, {feas})  ΔA {change:.3e}"
              + (f"  inner {inner}" if inner else ""))
        members_area = A_new
        env.current_step += 1
        if change < change_tol and compliance <= C_max:
            print(f"[MMA] converged at iteration {i}")
            break
    return members_area, np.array(compliance_hist), np.array(volume_hist)


def plot_history(compliance_hist, volume_hist, algo, out_path, C_max=None):
    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(11, 4), dpi=100)
